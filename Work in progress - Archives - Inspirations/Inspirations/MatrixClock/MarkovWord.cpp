char* CMarkovWord::Word(char* Buffer, int BufferSize, int MinLength, int MaxLength)
{
  int Length;
  do 
  {
    int Idx = 0;
    Buffer[0] = Buffer[1] = '{';
    do 
    {
      Buffer[Idx + 2] = NextChar(Buffer[Idx], Buffer[Idx + 1]);
      Idx++;
    } while (Buffer[Idx + 1] != '{' && Idx < (BufferSize - 1));
    Buffer[Idx + 1] = 0;

    Length = strlen(Buffer + 2);
  } while (Length < MinLength || Length > MaxLength);
  return Buffer + 2;
}

char CMarkovWord::NextChar(char First, char Second)
{
/*
table is a sequence of WORDs
  PAIR
  _CHR
  _CHR
   ...
  TRIPLE
  PAIR
  _CHR
  _CHR
   ...
  END

  PAIR is 0lll llss sssf ffff (<-lsb)
    fffff 5 bits of first char index
    sssss 5 bits of second char index
    0lllll 5 bits of length
  or if length would be 1:
  TRIPLE is 1ttt ttss sssf ffff (<-lsb)
    fffff 5 bits of first char index
    sssss 5 bits of second char index
    ttttt 5 bits of third char index (p = 0x7FF)

  _CHR is pppp pppp pppt tttt
    ttttt 5 bits of third char index
    ppppppppppp is 11 bits of "probability"/normalized count
  END is 0x0000
*/

  word Mask = 0x03FF;
  word Pair = PAIR(First, Second, 0);
  int ItemIdx = 0;
  word Item = pgm_read_word(kMarkovTable + ItemIdx);
  while (Item)
  {
    if ((Item & Mask) == Pair)
    {
      if (Item & 0x8000)
      {
        // triple
        return 'a' + ((Item >> 10) & 0x1F);
      }
      else
      {
        int Len = Item >> 10;
        word Rand = random(2048);
        word Sum = 0;
        for (int Ch = 0; Ch < Len; Ch++)
        {
          ItemIdx++;
          Item = pgm_read_word(kMarkovTable + ItemIdx);
          Sum += Item >> 5;
          if (Sum >= Rand)
          {
            return 'a' + (Item & 0x1F);
          }
        }
        // did not find the char?
        return '{';
      }
    }
    else
    {
      if (Item & 0x8000)
      {
        // triple
        ItemIdx++;
        Item = pgm_read_word(kMarkovTable + ItemIdx);
      }
      else
      {
        int Offs = (Item >> 10) + 1;
        ItemIdx += Offs;
        Item = pgm_read_word(kMarkovTable + ItemIdx);
      }
    }
  }
  // could not find the pair
  return '{';
}

